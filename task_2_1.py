# 1. Выяснить тип результата выражений:
# 15 * 3
# 15 / 3
# 15 // 2
# 15 ** 2

print("Результат выражения: 15 * 3 будет", type(15 * 3))
print("Результат выражения: 15 / 3 будет", type(15 / 3))
print("Результат выражения: 15 // 2 будет", type(15 // 2))
print("Результат выражения: 15 ** 2 будет", type(15 ** 2))
